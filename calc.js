export default class calc {
  add(a, b) {
    return a + b;
  }
  subtract(a, b) {
    return a - b;
  }
  multiply(a, b) {
    return a * b;
  }
  divide(a, b) {
    if (b===0) {
      throw new Error("cannot divide by zero");
    }
    return a / b;
  }
}

